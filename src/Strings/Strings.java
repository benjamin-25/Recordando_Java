package Strings;

public class Strings {
    /* la clase Strings es una clase especial, no es un tipo primitivo, ni un Wrapper
        es inmutable,
    */
    public static void main(String[] args) {

        String name = "Benjamin";

        System.out.println(name.toUpperCase());

        System.out.println(name);

        // Existe un pool de Strings, el cual no compara valores sino posiciones en memoria


    }
}
